const pup = async function(URL) {
    // const browser = await puppeteer.launch({ headless: false });
    const browser = await puppeteer.launch({ headless: true });
    const page = await browser.newPage();
    await page.goto(
        // 'https://techbookfest.org/event/tbf06/circle/61820002'
        URL
    );
    await page._client.send(
        'Input.synthesizeScrollGesture',
        {
            x: 0,
            y: 0,
            xDistance: 0,
            yDistance: -5000,
            repeatCount: 1,
            repeatDelayMs: 200

        }
    );

    let datas = await page.evaluate(() => {
        return document.documentElement.outerHTML;
    });

    // await fs.writeFile(path.join(DIRPATH, FILENAME), datas, (error) => {
    // await fs.writeFile(path.join(DIRPATH, URL), datas, (error) => {
    await fs.writeFile(path.join(DIRPATH, R.replace('https://techbookfest.org/event/tbf06/circle/', '', URL) ), datas, (error) => {
        console.log(error);
        if (error) {
            console.log('ファイル書き込みエラーです.');
        } else {
            console.log('ファイルに書き込みました.');
        }
    });

    await browser.close();
};

